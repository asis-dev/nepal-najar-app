export type PromiseStatus = 'not_started' | 'in_progress' | 'delivered' | 'stalled';
export type TrustLevel = 'verified' | 'partial' | 'unverified' | 'disputed';
export type SignalType = 'official' | 'discovered' | 'reported' | 'inferred';
export type CommitmentReviewState = 'candidate' | 'reviewed' | 'published' | 'rejected';
export type CommitmentScope = 'national' | 'provincial' | 'district' | 'municipal' | 'unknown';
export type PromiseCategory =
  | 'Governance'
  | 'Anti-Corruption'
  | 'Infrastructure'
  | 'Transport'
  | 'Energy'
  | 'Technology'
  | 'Health'
  | 'Education'
  | 'Environment'
  | 'Economy'
  | 'Social';

export interface GovernmentPromise {
  id: string;
  slug: string;
  title: string;
  title_ne: string;
  summary?: string;
  summary_ne?: string;
  category: PromiseCategory;
  category_ne: string;
  status: PromiseStatus;
  progress: number;
  linkedProjects: number;
  evidenceCount: number;
  lastUpdate: string;
  description: string;
  description_ne: string;
  trustLevel: TrustLevel;
  /** How the data was sourced */
  signalType: SignalType;
  /** Review boundary between discovered candidates and public commitments */
  reviewState?: CommitmentReviewState;
  /** Whether this record should appear on the public site */
  isPublic?: boolean;
  /** Canonical scope for the public read model */
  scope?: CommitmentScope;
  /** Public-facing responsible actors */
  actors?: string[];
  /** Number of distinct sources contributing evidence */
  sourceCount?: number;
  /** Most recent signal observed by the intelligence engine */
  lastSignalAt?: string;
  publishedAt?: string;
  originSignalId?: string;
  mergedIntoId?: string;
  reviewNotes?: string;
  deadline?: string;
  /** Estimated budget in NPR (lakhs) */
  estimatedBudgetNPR?: number;
  /** Budget spent so far in NPR (lakhs) */
  spentNPR?: number;
  /** Funding source */
  fundingSource?: string;
  fundingSource_ne?: string;
  /** Date of most recent daily activity (YYYY-MM-DD) */
  lastActivityDate?: string;
  /** Number of signals on the last activity date */
  lastActivitySignalCount?: number;
  /** Geographic impact scope */
  geoScope?: 'national' | 'provincial' | 'district' | 'municipal';
  /** Affected provinces (empty = all/national) */
  affectedProvinces?: string[];
  /** Affected districts (for district-scoped projects) */
  affectedDistricts?: string[];
  /** How far the benefit extends beyond the primary location */
  impactRadius?: 'local' | 'regional' | 'national';
  /** Where physical work/construction happens (for national projects) */
  primaryLocations?: Array<{ province: string; district?: string; description?: string }>;
  /** Baseline data from before new government — what they inherited */
  baselineProgress?: number;
  baselineStatus?: string;
  /** Live data confidence from signal analysis */
  liveDataConfidence?: 'insufficient' | 'partial' | 'sufficient';
}

export interface Deadline {
  id: string;
  label: string;
  label_ne: string;
  targetDate: string;
  type: 'legal' | 'policy' | 'budget' | 'milestone' | 'parliamentary';
  linkedPromiseIds: string[];
  description?: string;
  description_ne?: string;
}

export interface MockNewsArticle {
  id: string;
  headline: string;
  headline_ne: string;
  source_name: string;
  source_url: string;
  source_type: 'news' | 'government' | 'international' | 'social';
  published_at: string;
  excerpt: string;
  excerpt_ne: string;
  confidence: number;
  classification: 'confirms' | 'contradicts' | 'neutral';
  linkedPromiseIds: string[];
}

export interface TimelineEvent {
  date: string;
  title: string;
  title_ne: string;
  category: string;
  type: 'election' | 'ceremony' | 'governance' | 'policy' | 'finance' | 'milestone';
}

