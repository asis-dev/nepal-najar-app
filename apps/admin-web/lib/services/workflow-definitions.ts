import type { Service } from './types';

export type WorkflowMode = 'guide_only' | 'appointment' | 'payment' | 'mixed';

export interface WorkflowAction {
  id: string;
  label: string;
  label_ne?: string;
  kind: 'official_site' | 'payment' | 'appointment' | 'call' | 'directions';
  href?: string;
  completionLabel?: string;
  placeholder?: string;
  statusOnComplete?: 'ready' | 'in_progress' | 'booked' | 'submitted' | 'completed';
  stepOnComplete?: number;
  progressOnComplete?: number;
  nextActionOnComplete?: string;

  // ── Gold-standard fields ──────────────────────────────────────
  /** Fee hint shown to user, e.g. "Rs. 1,000–2,500" */
  estimatedFee?: string;
  /** Which docs the user needs for THIS step specifically */
  requiredDocs?: string[];
  /** Human-readable wait/duration estimate, e.g. "Instant", "2–8 weeks" */
  estimatedWait?: string;
  /** If true, user can skip this action (conditional/optional step) */
  canSkip?: boolean;
  /** Index into the parent milestones[] array — links action ↔ milestone */
  milestoneIndex?: number;
  /** AI can auto-complete this action given the right data (e.g. auto-pay, auto-fill) */
  aiAutoCompletable?: boolean;
  /** Prompt hint for AI to guide the user through this specific step */
  aiGuidancePrompt?: string;
}

export interface WorkflowDefinition {
  mode: WorkflowMode;
  requiresAppointment?: boolean;
  supportsOnlinePayment?: boolean;
  officeVisitRequired?: boolean;
  statusHint: string;
  statusHint_ne?: string;
  nextActionReady: string;
  nextActionReady_ne?: string;
  milestones: string[];
  milestones_ne?: string[];
  actions?: WorkflowAction[];
  /** AI can pre-fill form fields from user's identity profile for this workflow */
  aiCanPrefill?: boolean;
  /** AI prompt context for the overall workflow — helps the advisor give better guidance */
  aiWorkflowContext?: string;
}

