async function run() {
  const client = new Client({
    connectionString: process.env.DATABASE_URL,
    ssl: { rejectUnauthorized: false },
  });
  await client.connect();
  console.log('Connected. Updating article titles...');

  let updated = 0;
  for (const [numStr, title] of Object.entries(ARTICLE_TITLES)) {
    const num = parseInt(numStr);
    const { rowCount } = await client.query(
      `UPDATE constitution_articles
       SET article_title = $1, article_title_ne = $2
       WHERE article_number = $3 AND article_title LIKE 'Article %'`,
      [title, `धारा ${num}: ${title}`, num],
    );
    if (rowCount > 0) updated++;
  }

  console.log(`Updated ${updated} article titles`);
  await client.end();
}

run().catch(console.error);
